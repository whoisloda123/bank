package com.liucan.reflect;

/**
 * @author liucan
 * @version 18-10-29
 */
public class CalculateImpl implements ICalculate {
    @Override
    public int add(int a, int b) {
        return a + b;
    }

    @Override
    public int sub(int a, int b) {
        return a - b;
    }

    @Override
    public int mul(int a, int b) {
        return a * b;
    }

    @Override
    public int div(int a, int b) {
        return a / b;
    }
}
